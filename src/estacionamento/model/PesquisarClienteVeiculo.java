package estacionamento.model;

public class PesquisarClienteVeiculo {

    int idCliente;
    String condutor;
    boolean tipoCiente;
    double valorPagoCliente;
    int ativadoCliente;
    int idVeiculo;
    String placa;
    String cor;
    String modelo;
    String marca;
    int ativadoVeiculo;

    public int getIdCliente() {
        return idCliente;
    }

    public void setIdCliente(int idCliente) {
        this.idCliente = idCliente;
    }

    public String getCondutor() {
        return condutor;
    }

    public void setCondutor(String condutor) {
        this.condutor = condutor;
    }

    public boolean isTipoCiente() {
        return tipoCiente;
    }

    public void setTipoCiente(boolean tipoCiente) {
        this.tipoCiente = tipoCiente;
    }

    public double getValorPagoCliente() {
        return valorPagoCliente;
    }

    public void setValorPagoCliente(double valorPagoCliente) {
        this.valorPagoCliente = valorPagoCliente;
    }

    public int getAtivadoCliente() {
        return ativadoCliente;
    }

    public void setAtivadoCliente(int ativadoCliente) {
        this.ativadoCliente = ativadoCliente;
    }

    public int getIdVeiculo() {
        return idVeiculo;
    }

    public void setIdVeiculo(int idVeiculo) {
        this.idVeiculo = idVeiculo;
    }

    public String getPlaca() {
        return placa;
    }

    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getCor() {
        return cor;
    }

    public void setCor(String cor) {
        this.cor = cor;
    }

    public String getModelo() {
        return modelo;
    }

    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public String getMarca() {
        return marca;
    }

    public void setMarca(String marca) {
        this.marca = marca;
    }

    public int getAtivadoVeiculo() {
        return ativadoVeiculo;
    }

    public void setAtivadoVeiculo(int ativadoVeiculo) {
        this.ativadoVeiculo = ativadoVeiculo;
    }
    
    
    
}
