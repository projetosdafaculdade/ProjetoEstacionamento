    private void lerServicos() {        
        if (servicos != null) {
            if (servicos.getAtivado() == 1) {
                jtfFracao.setText(String.valueOf(servicos.getFracao()));
                jtfValorServidor.setText(String.valueOf(servicos.getValorServidor()));
                jtfValorPublico.setText(String.valueOf(servicos.getValorPublico()));
            }
        }
    }
